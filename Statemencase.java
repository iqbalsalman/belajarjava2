public class Statemencase{
	public static void main(String[] args){
		Integer value = 20;
		switch (value){
			case 10 :
			System.out.println("Nilai Sama dengan 10");
			break;
			case 20 :
			System.out.println("Nilai Sama dengan 20");
			break;
			default : System.out.println("Nilai tidak ada");
		}

	}
}